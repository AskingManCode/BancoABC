CREATE TABLE TRANSACCIONES_TB(
	TRAN_ID INT PRIMARY KEY IDENTITY(1,1),
	TRAN_CUENTA_ID INT NOT NULL, -- FK
	TRAN_TIPO_TRAN_ID INT NOT NULL, -- FK
	TRAN_MontoRetiro DECIMAL(10,2),
	TRAN_CodAutorizacion VARCHAR(50),
	TRAN_FechaHora DATETIME NOT NULL DEFAULT GETDATE(),

	FOREIGN KEY(TRAN_CUENTA_ID)
		REFERENCES CUENTAS_TB(CUENTA_ID),
	FOREIGN KEY(TRAN_TIPO_TRAN_ID)
		REFERENCES TIPOS_TRANSACCIONES_TB(TIPO_TRAN_ID)
);

