CREATE TABLE CUENTAS_TB(
	CUENTA_ID INT PRIMARY KEY NOT NULL,
	CUENTA_Tarjeta VARCHAR(25) NOT NULL,
	CUENTA_MontoDisponible DECIMAL(10,2) NOT NULL,
	CUENTA_Estado BIT NOT NULL DEFAULT 1,

	CONSTRAINT UQ_CUENTA_TARJETA
		UNIQUE(CUENTA_Tarjeta)
);

SELECT *
FROM CUENTAS_TB;

