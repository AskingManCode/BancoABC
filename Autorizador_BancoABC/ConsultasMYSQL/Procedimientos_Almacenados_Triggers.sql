	AND TAR.TARJETA_Estado = 1;
    


-- AUT3_CambioPIN
DELIMITER //

CREATE PROCEDURE SP_AUT3_CAMBIOPIN(
    IN P_COD_CAJERO VARCHAR(15),
    IN P_NUM_TARJETA VARCHAR(255),
    IN P_PIN_ACTUAL VARCHAR(255),
    IN P_PIN_NUEVO VARCHAR(255),
    
    OUT P_RESPUESTA_OUT INT
)
BEGIN
    PROC: BEGIN

        DECLARE V_TARJETA_ID INT;
        DECLARE V_CAJERO_ID INT;
        DECLARE V_TRANSACCION_ID INT;

        DECLARE EXIT HANDLER FOR SQLEXCEPTION
        BEGIN
            ROLLBACK;
            SET P_RESPUESTA_OUT = 5; -- ERROR NO CONTROLADO
        END;

        -- VALIDAR CAJERO
        SELECT CAJ_ID
        INTO V_CAJERO_ID
        FROM CAJEROS_TB
        WHERE CAJ_ID_Identificador = P_COD_CAJERO
          AND CAJ_Estado = 1
        LIMIT 1;

        IF V_CAJERO_ID IS NULL THEN
            SET P_RESPUESTA_OUT = 2; -- DATOS INCORRECTOS
            LEAVE PROC;
        END IF;

        -- VALIDAR TARJETA Y PIN
        SELECT TARJETA_ID
        INTO V_TARJETA_ID
        FROM TARJETAS_TB
        WHERE TARJETA_Numero = P_NUM_TARJETA
          AND TARJETA_PIN = P_PIN_ACTUAL
          AND TARJETA_Estado = 1
        LIMIT 1;

        IF V_TARJETA_ID IS NULL THEN
            SET P_RESPUESTA_OUT = 2; -- DATOS INCORRECTOS
            LEAVE PROC;
        END IF;

        START TRANSACTION;

        -- CAMBIO DE PIN
        UPDATE TARJETAS_TB
        SET TARJETA_PIN = P_PIN_NUEVO
        WHERE TARJETA_ID = V_TARJETA_ID;

        -- REGISTRO DE LA TRANSACCIÓN
        INSERT INTO TRANSACCIONES_TB (
            TRANSAC_CUENTA_ID,
            TRANSAC_TIPO_TRANSAC_ID,
            TRANSAC_MONTO,
            TRANSAC_ESTADO_ID
        )
        SELECT
            CU.CUENTA_ID,
            TT.TIPO_TRANSAC_ID,
            0.00,
            ET.EST_TRANSAC_ID
        FROM CUENTAS_TB CU
        JOIN TIPOS_TRANSACCIONES_TB TT
            ON TT.TIPO_TRANSAC_Nombre = 'Cambio de PIN'
        JOIN ESTADOS_TRANSACCIONES_TB ET
            ON ET.EST_TRANSAC_Nombre = 'Completada'
        WHERE CU.CUENTA_TARJETA_ID = V_TARJETA_ID;

        SET V_TRANSACCION_ID = LAST_INSERT_ID();

        -- RELACIÓN TRANSACCIÓN / CAJERO
        INSERT INTO TRANSACCIONES_CAJEROS_TB (
            TRANSAC_CAJ_CAJ_ID,
            TRANSAC_CAJ_TRANSAC_ID,
            TRANSAC_FechaHora
        )
        VALUES (
            V_CAJERO_ID,
            V_TRANSACCION_ID,
            NOW()
        );

        COMMIT;
        SET P_RESPUESTA_OUT = 0; -- OK

    END PROC;
END//

DELIMITER //

CREATE PROCEDURE REGISTRAR_PERSONA_SP(
    IN IDENTIFICACION VARCHAR(25),
    IN NOMBRE VARCHAR(150),
    IN PRIMER_APELLIDO VARCHAR(150),
    IN SEGUNDO_APELLIDO VARCHAR(150),
    IN CORREO VARCHAR(150)
)
BEGIN
    -- Validación: campos obligatorios no vacíos
    IF TRIM(IDENTIFICACION) = '' OR IDENTIFICACION IS NULL THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: La identificación es obligatoria.';
    END IF;

    IF TRIM(NOMBRE) = '' OR NOMBRE IS NULL THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: El nombre es obligatorio.';
    END IF;

    IF TRIM(PRIMER_APELLIDO) = '' OR PRIMER_APELLIDO IS NULL THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: El primer apellido es obligatorio.';
    END IF;

    IF TRIM(CORREO) = '' OR CORREO IS NULL THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: El correo es obligatorio.';
    END IF;

    -- Validación: formato de correo básico
    IF CORREO NOT LIKE '%_@__%.__%' THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: El formato del correo no es válido.';
    END IF;

    -- Validación: identificación no duplicada
    IF EXISTS (SELECT 1 FROM PERSONAS_TB WHERE PER_IDENTIFICACION = IDENTIFICACION) THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: La identificación ya está registrada.';
    END IF;

    -- Validación: correo no duplicado
    IF EXISTS (SELECT 1 FROM PERSONAS_TB WHERE PER_CORREO = CORREO) THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Error: El correo ya está registrado.';
    END IF;

    -- INSERT
    INSERT INTO PERSONAS_TB (
        PER_IDENTIFICACION,
        PER_NOMBRE,
        PER_APELLIDO1,
        PERS_APELLIDO2,
        PER_CORREO
    ) VALUES (
        TRIM(IDENTIFICACION),
        TRIM(NOMBRE),
        TRIM(PRIMER_APELLIDO),
        TRIM(SEGUNDO_APELLIDO),
        TRIM(CORREO)
    );

END //

DELIMITER ;




