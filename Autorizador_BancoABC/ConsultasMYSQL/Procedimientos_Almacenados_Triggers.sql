-- AUT5 - SP PARA TARJETAS DE CREDITO
DELIMITER //

CREATE PROCEDURE SP_AUT5_ADELANTO_EFECTIVO(
    IN P_NUMERO_TARJETA VARCHAR(255), -- Cifrado
    IN P_IDENTIFICADOR_CAJERO VARCHAR(15), -- C
    IN P_TIPO_TRANSACCION VARCHAR(15), --
    IN P_MONTO_TRANSACCION DECIMAL(10,2),
    
    OUT P_RESPUESTA INT
)
BEGIN
	PROC: BEGIN
		
		DECLARE V_TARJETA_ID INT;
		DECLARE V_CAJERO_ID INT;
		DECLARE V_TRANSACCION_ID INT;
		
		-- VALIDAR CAJERO
		SELECT CAJ_ID
		INTO V_CAJERO_ID
		FROM CAJEROS_TB
		WHERE CAJ_ID_Identificador = P_IDENTIFICADOR_CAJERO
		  AND CAJ_Estado = 1
		LIMIT 1;

		IF V_CAJERO_ID IS NULL THEN
			SET P_RESPUESTA = 2; -- DATOS INCORRECTOS
			LEAVE PROC;
		END IF;
		
		-- VALIDAR TARJETA
		SELECT TARJETA_ID
		INTO V_TARJETA_ID
		FROM TARJETAS_TB
		WHERE TARJETA_Numero = P_NUMERO_TARJETA
			AND TARJETA_Estado = 1
		LIMIT 1;
		
		IF V_TARJETA_ID IS NULL THEN
			SET P_RESPUESTA = 2; -- DATOS INCORRECTOS
			LEAVE PROC;
		END IF;
			
		-- VALIDAR TIPO DE TRANSACCIÓN
		IF UPPER(
			   REPLACE(P_TIPO_TRANSACCION, 'Ó', 'O')
		) != 'CONFIRMACION' THEN
			SET P_RESPUESTA = 5; -- ERROR NO CONTROLADO
			LEAVE PROC;
		END IF;
		
		-- Valida monto de la tarjeta
		IF NOT EXISTS (
			SELECT 1
			FROM CUENTAS_TB
			WHERE CUENTA_TARJETA_ID = V_TARJETA_ID
			  AND CUENTA_MontoAdelantadoEfectivo >= P_MONTO_TRANSACCION
		) THEN
			SET P_RESPUESTA = 1; -- FONDOS INSUFICIENTES
			LEAVE PROC;
		END IF;
		
		START TRANSACTION;
		
		-- Actualiza el monto de adelanto de efectivo
		UPDATE CUENTAS_TB 
		SET CUENTA_MontoAdelantadoEfectivo = 
			CUENTA_MontoAdelantadoEfectivo - P_MONTO_TRANSACCION
		WHERE CUENTA_TARJETA_ID = V_TARJETA_ID;
		
		INSERT INTO TRANSACCIONES_TB (
			TRANSAC_CUENTA_ID, 
			TRANSAC_TIPO_TRANSAC_ID, 
			TRANSAC_MONTO, 
			TRANSAC_ESTADO_ID
		) 
		SELECT
		CU.CUENTA_ID,
		TT.TIPO_TRANSAC_ID,
		P_MONTO_TRANSACCION,
		ET.EST_TRANSAC_ID
		FROM CUENTAS_TB CU
		JOIN TIPOS_TRANSACCIONES_TB TT
			ON TT.TIPO_TRANSAC_Nombre = 'Retiro'
		JOIN ESTADOS_TRANSACCIONES_TB ET
			ON ET.EST_TRANSAC_Nombre = 'Pendiente'
		WHERE CU.CUENTA_TARJETA_ID = V_TARJETA_ID;
		
		SET V_TRANSACCION_ID = LAST_INSERT_ID();
		
		INSERT INTO TRANSACCIONES_CAJEROS_TB (
		TRANSAC_CAJ_CAJ_ID,
		TRANSAC_CAJ_TRANSAC_ID,
		TRANSAC_FechaHora
		)
		VALUES (
			V_CAJERO_ID,
			V_TRANSACCION_ID,
			NOW()
		);
		
		COMMIT;
		SET P_RESPUESTA = 0; -- OK
		
	END PROC;
END//

DELIMITER ;


